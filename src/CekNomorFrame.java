    private void tblChkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tblChkActionPerformed
        // TODO add your handling code here:
        try {
            int angka = Integer.parseInt(txtAngka.getText());
            String hasil;
            Color warnaHasil = new Color(236, 240, 241); // warna teks default terang

            if (angka % 2 == 0) {
                hasil = "Angka " + angka + " adalah Bilangan Genap ";
                warnaHasil = new Color(46, 204, 113); // hijau (genap)
            } else {
                hasil = "Angka " + angka + " adalah Bilangan Ganjil ";
                warnaHasil = new Color(231, 76, 60); // merah (#E74C3C)
            }

            // Cek tambahan jika checkbox prima dicentang
            if (chkPrima.isSelected()) {
                if (isPrima(angka)) {
                    hasil += "\ndan Angka ini merupakan Bilangan Prima.";
                    warnaHasil = new Color(241, 196, 15); // emas
                } else {
                    hasil += "\ndan Angka ini Bukan Bilangan Prima.";
                    warnaHasil = new Color(231, 76, 60); // merah
                }
            }
            // Tampilkan hasil
        txtHasil.setForeground(warnaHasil); // ubah warna teks hasil
        txtHasil.setText(hasil);

            // Tampilkan hasil dengan JOptionPane
            JOptionPane.showMessageDialog(this, hasil, "Hasil", JOptionPane.INFORMATION_MESSAGE);
            txtHasil.setText(hasil);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Input tidak valid! Harap masukkan angka.", 
                    "Error", JOptionPane.ERROR_MESSAGE);
            txtAngka.requestFocus();
          
        }
        
    }                                      

    // Fungsi untuk cek bilangan prima
    private boolean isPrima(int n) {
        if (n <= 1) return false;
        for (int i = 2; i <= Math.sqrt(n); i++) {
            if (n % i == 0) return false;
        }
        return true;
    }//GEN-LAST:event_tblChkActionPerformed

